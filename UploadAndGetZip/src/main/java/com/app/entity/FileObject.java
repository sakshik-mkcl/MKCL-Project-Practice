package com.app.entity;

public class FileObject {
	int id;
	String name;
	
	
	public FileObject() {
		
	}
	public FileObject( String name) {
		
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
